public class MinMax {
    public static void main(String[] args) {

        int num1 = 25;
        int num2 = 40;

        if (num1 < num2) {
            System.out.println("Minimum number is: " + num1);
        } else {
            System.out.println("Minimum number is: " + num2);
        }

        if (num1 > num2) {
            System.out.println("Maximum number is: " + num1);
        } else {
            System.out.println("Maximum number is: " + num2);
        }
    }
}